package com.cognizant.cohort57.pod2.model;

public class PetInfo {

	private int petId;

	private int userId;

	private String petName;

	private double petAge;

	private String petBreed;

	private String petGender;

	private String petHairCoatType;

	private double petWeight;

	private String petColor;

	public int getPetId() {
		return petId;
	}

	public void setPetId(int petId) {
		this.petId = petId;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getPetName() {
		return petName;
	}

	public void setPetName(String petName) {
		this.petName = petName;
	}

	public double getPetAge() {
		return petAge;
	}

	public void setPetAge(double petAge) {
		this.petAge = petAge;
	}

	public String getPetBreed() {
		return petBreed;
	}

	public void setPetBreed(String petBreed) {
		this.petBreed = petBreed;
	}

	public String getPetGender() {
		return petGender;
	}

	public void setPetGender(String petGender) {
		this.petGender = petGender;
	}

	public String getPetHairCoatType() {
		return petHairCoatType;
	}

	public void setPetHairCoatType(String petHairCoatType) {
		this.petHairCoatType = petHairCoatType;
	}

	public double getPetWeight() {
		return petWeight;
	}

	public void setPetWeight(double petWeight) {
		this.petWeight = petWeight;
	}

	public String getPetColor() {
		return petColor;
	}

	public void setPetColor(String petColor) {
		this.petColor = petColor;
	}

}
